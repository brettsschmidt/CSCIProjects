
public class Point 
{
	// Variables
	private float m_x;
	private float m_y;
	
	//Constructor
	public Point(float x, float y) {
		m_x = x;
		m_y = y;
	}
	
	// Getter
	public float getX() 
	{
		return m_x;
	}
	
	// Setter
	public void setX(float m_x) 
	{
		this.m_x = m_x;
	}
	
	// Getter
	public float getY() 
	{
		return m_y;
	}
	
	// Setter
	public void setY(float m_y) 
	{
		this.m_y = m_y;
	}
}
